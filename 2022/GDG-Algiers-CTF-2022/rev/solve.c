char pass[] = {0x15, 0x91, 0x2a, 0x59, 0x72, 0x1e, 0xd9, 0xa, 0xb6, 0xf1, 0x2a, 0xba, 0x5f, 0x66, 0x70, 0x61, 0x4f, 0xf7, 0xd1, 0x49, 0xd6, 0xac, 0xb4, 0x21, 0xb2, 0x1e, 0x94, 0x28, 0x5a, 0x57, 0xaa, 0x15, 0xc7, 0xa, 0xc8, 0xa3, 0xf0, 0x76, 3, 0x34, 0x88, 0xe1, 0x24, 0x63, 0xc2, 0x13, 0x5a};

int main(void){
    int i = 0;
    char clear;
    srand(0x7e6);
    for(i = 0; i < sizeof(pass); i++){
        int k1 = rand();
        int k2 = ((k1 >> 0x1f) >> 0x18);
        clear = (pass[i] + k2) ^ (k1 + k2);
        putchar(clear);
    }
    return 0;
}
